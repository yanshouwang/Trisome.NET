namespace Trisome.Core.Logging
{
    /// <summary>
    /// Defines values for the categories used by <see cref="ILogger"/>.
    /// </summary>
    public enum Category
    {
        /// <summary>
        /// Debug category.
        /// </summary>
        Debug,
        /// <summary>
        /// Informational category.
        /// </summary>
        Info,
        /// <summary>
        /// Warning category.
        /// </summary>
        Warn,
        /// <summary>
        /// Exception category.
        /// </summary>
        Error
    }
}